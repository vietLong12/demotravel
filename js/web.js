const countDownDate = new Date("February 10, 2023 00:00:00").getTime();

setInterval(function() {
  const now = new Date().getTime();

  const distance = countDownDate - now;

  const days = Math.floor(distance / (1000 * 60 * 60 * 24));
  const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
  const seconds = Math.floor((distance % (1000 * 60)) / 1000);

  document.getElementById("countdown").innerHTML = "Còn" +" "+days +" "+ "ngày " + hours + "h "
  + minutes + "m " + seconds + "s ";
  document.getElementById("countdown1").innerHTML = "Còn" +" "+days +" "+ "ngày " + hours + "h "
  + minutes + "m " + seconds + "s ";
  document.getElementById("countdown2").innerHTML = "Còn" +" "+days +" "+ "ngày " + hours + "h "
  + minutes + "m " + seconds + "s ";
}, 1000);

